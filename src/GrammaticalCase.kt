enum class GrammaticalCase {
    NOMINATIVE, // mianownik; kto? co?
    GENITIVE, // dopełniacz; kogo? czego? (nie ma)
    DATIVE, // celownik; komu? czemu? (się przyglądam)
    ACCUSATIVE, // biernik; kogo? co? (widzę)
    INSTRUMENTAL, // narzędnik; (z) kim? (z) czym? (idę)
    LOCATIVE, // miejscownik; o kim? o czym? (mówię)
    VOCATIVE // wołacz; zwrot do kogoś lub czegoś
}